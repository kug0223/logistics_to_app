        (app) => app.toId == widget.to.id,
        orElse: () => throw Exception('Not found'),
      );

      setState(() {
        _applicationStatus = myApplication.status;
        _isLoading = false;
      });
    } catch (e) {
      // ÏßÄÏõê ÎÇ¥Ïó≠Ïù¥ ÏóÜÏúºÎ©¥ nullÎ°ú ÏÑ§Ï†ï
      setState(() {
        _applicationStatus = null;
        _isLoading = false;
      });
    }
  }

