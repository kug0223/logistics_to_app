  Future<void> _checkApplicationStatus() async {
    final userProvider = Provider.of<UserProvider>(context, listen: false);
    final uid = userProvider.currentUser?.uid;

    if (uid == null) {
      setState(() {
        _isLoading = false;
      });
      return;
