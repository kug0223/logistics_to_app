    try {
      final myApplications = await _firestoreService.getMyApplications(uid);
      
      // ÌòÑÏû¨ TOÏóê ÎåÄÌïú ÏßÄÏõê ÎÇ¥Ïó≠ Ï∞æÍ∏∞
